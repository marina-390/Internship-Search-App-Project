function t(key) {
  const lang = localStorage.getItem('lang') || 'en';
  const keys = key.split('.');
  let result = (TRANSLATIONS[lang] || TRANSLATIONS.en);
  for (const k of keys) result = result?.[k];
  if (result !== undefined) return result;
  let fallback = TRANSLATIONS.en;
  for (const k of keys) fallback = fallback?.[k];
  return fallback ?? key;
}

function setLanguage(lang) {
  localStorage.setItem('lang', lang);
  document.querySelectorAll('.lang-switcher.open').forEach(el => el.classList.remove('open'));
  applyTranslations();
}

function toggleLangDropdown(e) {
  e.stopPropagation();
  const switcher = e.currentTarget.closest('.lang-switcher');
  const isOpen = switcher.classList.contains('open');
  document.querySelectorAll('.lang-switcher.open').forEach(el => el.classList.remove('open'));
  if (!isOpen) switcher.classList.add('open');
}

function applyTranslations() {
  const lang = localStorage.getItem('lang') || 'en';
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = t(el.dataset.i18n);
    if (val) el.textContent = val;
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const val = t(el.dataset.i18nPlaceholder);
    if (val) el.placeholder = val;
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const val = t(el.dataset.i18nHtml);
    if (val) el.innerHTML = val;
  });
  document.querySelectorAll('.lang-current').forEach(el => {
    el.textContent = lang.toUpperCase();
  });
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
  const langPrefSelect = document.getElementById('langPrefSelect');
  if (langPrefSelect) langPrefSelect.value = lang;
  if (typeof updateShareCards === 'function') updateShareCards();
  if (typeof rerenderApplicantCards === 'function') rerenderApplicantCards();
  if (typeof rerenderCompanyApplications === 'function') rerenderCompanyApplications();
  if (typeof rerenderCompanyPostings === 'function') rerenderCompanyPostings();
}

document.addEventListener('DOMContentLoaded', applyTranslations);
document.addEventListener('click', () => {
  document.querySelectorAll('.lang-switcher.open').forEach(el => el.classList.remove('open'));
});
